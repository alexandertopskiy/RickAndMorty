import SwiftUI

extension Font {

    static func appFontBold(ofSize size: CGFloat) -> Font {
        return Font.custom("Gilroy-Bold", size: size)
    }

    static func appFontSemibold(ofSize size: CGFloat) -> Font {
        return Font.custom("Gilroy-Semibold", size: size)
    }

    static func appFontMedium(ofSize size: CGFloat) -> Font {
        return Font.custom("Gilroy-Medium", size: size)
    }

}
